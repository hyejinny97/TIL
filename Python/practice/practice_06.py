# 접근 방법
'''
1. 문제 21번과 같은 방법으로 각 자리수의 값을 꺼내올 수 있음
2. 꺼내온 각 자릿수의 값을 차례로 출력
'''
# 코드 1
N = 1234
while N:
    print(N % 10, end='')
    N //= 10

# 코드 2
N = 1234
rst = 0
while N:
    rst *= 10
    rst += N % 10
    N //= 10
print(rst)