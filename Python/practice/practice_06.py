# 문제20: 각 자릿수의 합 구하기
# 문제: 정수 number가 주어질 때, 각 자릿수의 합을 구해서 출력하세요. 
# 접근방법
'''
1. 10으로 나눈 나머지를 구하면 첫째 자릿수의 값을 알 수 있다.
2. 10으로 나눈 몫을 구하면 첫째 자릿수 이상의 값을 알 수 있다.
3. 따라서 10으로 나눈 나머지를 rst변수에 계속 더해주고, 동시에 10으로 나눈 몫은 인풋값을 받은 N 변수에 계속 할당해준다.
4. 변수 N이 0이 될때까지 3번 과정을 반복한다.
'''
# 코드 1
N = 123
rst = 0
while N:
    rst += N % 10
    N //= 10
print(rst)

# 코드 2
N = 123
rst = 0
while N:
    N, remainder = divmod(N, 10)
    rst += remainder
print(rst)




